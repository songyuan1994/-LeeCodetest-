package com.example.demo;

public class BankNameCode {

	private String bankName;
	private String bankCode;
	private String bankArea;
	private String bankAreaCode;
	private String bankCityCode;
	private String bankcity;
	private String unionBankName;
	private String unionBankCode;

	public String getBankName() {
		return bankName;
	}

	public void setBankName(String bankName) {
		this.bankName = bankName;
	}

	public String getBankCode() {
		return bankCode;
	}

	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}

	public String getBankArea() {
		return bankArea;
	}

	public void setBankArea(String bankArea) {
		this.bankArea = bankArea;
	}

	public String getBankAreaCode() {
		return bankAreaCode;
	}

	public void setBankAreaCode(String bankAreaCode) {
		this.bankAreaCode = bankAreaCode;
	}

	public String getBankCityCode() {
		return bankCityCode;
	}

	public void setBankCityCode(String bankCityCode) {
		this.bankCityCode = bankCityCode;
	}

	public String getBankcity() {
		return bankcity;
	}

	public void setBankcity(String bankcity) {
		this.bankcity = bankcity;
	}

	public String getUnionBankName() {
		return unionBankName;
	}

	public void setUnionBankName(String unionBankName) {
		this.unionBankName = unionBankName;
	}

	public String getUnionBankCode() {
		return unionBankCode;
	}

	public void setUnionBankCode(String unionBankCode) {
		this.unionBankCode = unionBankCode;
	}

}
